# Λύση equality-QP υποπροβλήματος με KKT
def solve_equality_qp_kkt(G, grad, Aeq):
    """
    Λύνει:
        min_p 1/2 p^T G p + grad^T p
        s.t. Aeq p = 0

    Με σύμβαση:
        L = f - lambda^T(Ax - b)

    KKT:
        Gp + grad - Aeq^T lambda = 0
        Aeq p = 0

    Δηλαδή:
        [G  -Aeq^T] [p     ] = [-grad]
        [Aeq   0  ] [lambda]   [  0  ]
    """

    n = G.shape[0]
    meq = Aeq.shape[0]

    if meq == 0:
        p = -np.linalg.solve(G, grad)
        lambda_eq = np.array([])
        return p, lambda_eq

    KKT = np.block([
        [G, -Aeq.T],
        [Aeq, np.zeros((meq, meq))]
    ])

    rhs = np.concatenate([-grad, np.zeros(meq)])
    sol = np.linalg.solve(KKT, rhs)

    p = sol[:n]
    lambda_eq = sol[n:]

    return p, lambda_eq


