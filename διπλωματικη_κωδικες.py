def projected_cg(G, b, A, g, x0, tol=1e-10, maxit=100):

    n = G.shape[0]

    AAT = A @ A.T
    P = np.eye(n) - A.T @ np.linalg.solve(AAT, A)

    x = x0.copy()

    r = G @ x - b
    z = P @ r
    d = -z

    res_hist = [np.linalg.norm(z)]
    obj_hist = [0.5 * x.T @ G @ x - b.T @ x]
    feas_hist = [np.linalg.norm(A @ x - g)]

